


 class Student {
    int id;
    String name;

    Student(int i, String n) {
        id = i;
        name = n;
    }

    void display() {
        System.out.println(id + " " + name);
    }
}

public class studentconstructordemo {

    public static void main(String[] args) {
        Student s1 = new Student(1, "Ram");
        s1.display();
    }
}


