package day10_revision;

public class ArithmetcOperations {
    public static void main(String[] args) {
        int  answer = 2 +2;
        System.out.println(answer);
        answer = answer -1;
        System.out.println(answer);
        answer = answer *2;
        System.out.println(answer);
        answer = answer /2;
        System.out.println(answer);
        answer = answer +8;
        System.out.println(answer);
        answer = answer %7;
        System.out.println(answer);
        
    }
    
}
