package day10_revision;

public class BitwiseOperator {

    public static void main(String[] args) {
        int A = 10; // 1010 in binary
        int B = 3;  // 0011 in binary
        int Y;
        Y = A & B;
        System.out.println(Y); 
        Y = A | B;
        System.out.println(Y);
        Y = A ^ B;
        System.out.println(Y);



    }
    
}
